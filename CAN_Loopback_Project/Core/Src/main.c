/* USER CODE BEGIN 4 */
void Send_RPM_Message(uint16_t rpm_value)
{
    CAN_TxHeaderTypeDef TxHeader;
    uint8_t TxData[8];
    uint32_t TxMailbox;

    // 1. Configure the Message Header
    TxHeader.StdId = 0x103;             // The "ID" (Engine RPM)
    TxHeader.RTR = CAN_RTR_DATA;        // We are sending Data, not recieving
    TxHeader.IDE = CAN_ID_STD;          // (11-bit) id standard
    TxHeader.DLC = 2;                   // Sending 2 Bytes fore rpm
    TxHeader.TransmitGlobalTime = DISABLE;


    TxData[0] = (rpm_value >> 8) & 0xFF; // High Byte (0x09)
    TxData[1] = rpm_value & 0xFF;        // Low Byte  (0xC4)

    // 3. Send it!
    if (HAL_CAN_AddTxMessage(&hcan1, &TxHeader, TxData, &TxMailbox) != HAL_OK)
    {
        Error_Handler(); // If this triggers, the Mailbox is full or CAN isn't started
    }
}

void Send_Fault_Message(void){
	CAN_TxHeaderTypeDef TxHeader;
	uint8_t TxData[1];
	uint32_t TxMailbox;

	TxHeader.StdId = 0x001;
	TxHeader.ExtId = 0x01;
	TxHeader.RTR = CAN_RTR_DATA;
	TxHeader.IDE = CAN_ID_STD;
	TxHeader.DLC = 1;

	TxData[0] = 0xFF;             //Error code for engine

	HAL_CAN_AddTxMessage(&hcan1, &TxHeader, TxData, &TxMailbox);
}

void HAL_CAN_RxFifo0MsgPendingCallback(CAN_HandleTypeDef *hcan)
{
  CAN_RxHeaderTypeDef RxHeader;
  uint8_t RxData[8];

  // 1. Get message from fifo to allow sport mode to turn on led
  if (HAL_CAN_GetRxMessage(hcan, CAN_RX_FIFO0, &RxHeader, RxData) == HAL_OK)
  {
      //Check if the ID matches "Sport Mode" (ID 0x200)
      if (RxHeader.StdId == 0x200)
      {
          //  if one turn on
          if (RxData[0] == 1)
          {
            HAL_GPIO_WritePin(LD2_GPIO_Port, LD2_Pin, GPIO_PIN_SET); // led on
          }
          else
          {
            HAL_GPIO_WritePin(LD2_GPIO_Port, LD2_Pin, GPIO_PIN_RESET); // led off
          }
      }
  }
}
